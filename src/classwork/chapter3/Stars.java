package classwork.chapter3;

class Stars {
    public static void main(String[] args) {
        for (int j = 0; j < 6; j++) {
            for (int i = 0; i < 6; i++) {
                System.out.print("*");
            }
            System.out.println();
        }
        System.out.println("\n\n\n");

        for (int x = 0; x < 6; x++) {
            System.out.println("******");
        }
        System.out.println("\n\n\n");

        for (int i = 6; i > 0 ; i--) {
            System.out.println("******");
        }

    }
}


